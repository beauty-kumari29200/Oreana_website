                                <p>In the rapidly advancing world, the organization not only relies on voice calls, now they use other modes of communication, which include video conferencing, desktop sharing, messaging, etc. All these elements are intensively used during the Corona pandemic. They required a protocol called Session Initiation Protocol, to work flawlessly. Even though the Session Initiation Protocol is a powerful and integrated part of real-time communications, but has a lot of challenges, which are increases during the Corona pandemic. To overcome those challenges Session Border Controller is used.</p>
                                <p style="font-weight:bold; text-decoration: underline;">1. What is SBC?</p>
                                <p>A Session Border Controller is a device use to regulate IP combination flows. You deploy it at the network border to regulate all forms of communication.</p>
                                <p>For sake of simplicity, we can say SBC is like a firewall that controls the flow of information between call endpoints, restricting end-to-end transparency. The Session Border Controller consists of three words:</p>

                                <p style="font-weight:bold; text-align: center;">SESSION + BORDER + CONTROLLER</p>
                                
