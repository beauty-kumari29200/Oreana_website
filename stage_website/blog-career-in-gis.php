                                <p style="font-weight:bold">3. Skills required to Learn GIS</p>
                                <p>Qualification and skills matter a lot for a graduate who is willing to pursue the GIS course. It is best suited for the student having an understanding of environmental science, geography, wildlife, botany, forestry, ecology, etc., apart from education, skills of programming, database, drawing, and visualization are also advantageous for the candidates willing to pursue a course in GIS for career development.</p>

                                <p style="font-weight:bold">4. Career opportunity in GIS</p>
                                <p>Nowadays, due to the intensive use of GIS, not only the Government but also private organizations hire GIS experts. The most successful GIS professional build their career with multiple skills set and become:</p>

                                <p> <strong>I. GIS Manager:</strong> You need to have a broad knowledge of GIS applications, compatibilities, and system requirements. You will also be responsible for planning the overall structure of GIS within the organization.</p>

                                <p> <strong>II. GIS Analyst:</strong> You require specific knowledge of computer operating systems, GIS software and its application, GIS concepts, and GIS database application design concepts. As a GIS analyst, you will have to conduct case analysis of business functions to establish GIS application requirements, plan and designing the analytical procedure and methodologies, and cost estimation for GIS development activities.</p>

                                <p> <strong>III. Map Designer:</strong> You need to have a strong knowledge of map design principles, graphic composition, and map production methods. You have to compile and integrate map data for automation from different resources and design high-quality maps and graphic products for output.</p>

                                <p> <strong>IV.	GIS Application Developer:</strong> Being a GIS application developer means you have to create a custom application to meet specific user’s needs.</p>

                                <p> <strong>V. Database Administrator:</strong> The database administrator is primarily responsible for the spatial and tabular database of the GIS. You will oversee the data automation and maintenance and be involved in detailed database design and maintenance of standards related to data entry and updating.</p>

                                <p> <strong> VI. GIS Coordinator:</strong> Being a GIS coordinator you will have to plan, organize, coordinate, and participates in the development and implementation of the organization’s GIS to meet end-user service objectives.</p>

