                                <p>A Fresher is someone who has just graduated from a college or university and is looking to enter into
                                    the workforce. For the same, he must build a resume that would stand out in the eyes of the
                                    recruiters and hiring managers. To showcase what a proper resume looks like, let us have a look at
                                    the formats for a good resume as well as certain tips and tricks.</p>
                                    
                                    
                                <h5>A well-crafted resume would consider the following points:</h5>
                                <br>
                                <p> <b>1. The Highlighting Features:</b></br> A fresher is someone who has little to no work experience.
                                    Therefore, it is essential for the person to highlight their assets by bringing emphasis on the
                                    person’s skills and abilities to make up for the lack of experience. While reviewing a fresher’s
                                    resume, the managers would look at these skills and analyze whether they are a right fit for
                                    the job or not. It would help attract the attention of companies and sell the talents, aptitude
                                    or strongest skills of the fresher.</p>
                                    
                                    
                                  
