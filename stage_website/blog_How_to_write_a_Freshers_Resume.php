                                <b>2. Doing a Proper Research of the Company: </b> 

                                    <p>If you wish to sell yourself to the company then
                                    you would need to delve deep into the company that you wish to apply for and do proper
                                    research of what exactly that company is looking for. Try to match your career objectives
                                    and qualifications to your potential employers’ requirements. You must pay attention to the
                                    type of job you wish to target, the needs of the employers’ and the career pathway that you
                                    wish to carve.</p>
                                    <div class="col-lg-6">
                                        <div class="card-body-img">
                                            <img width="50%" src="https://apollo-singapore.akamaized.net/v1/files/e1bcfw34307n-PK/image" alt="blog-img">
                                        </div>
                                    </div></br>
                                <b>3. Reading the Job Description:</b>
                                <p>A job description would mention the specific skillset required
                                    for the job. You must make a note of the keywords as it would help you to include the same
                                    throughout your own resume. This would show that you have the same set of core values as
                                    the employer.</p>
                                    
                                     <p><b>4. Customize your Resume as per Required:</b></br>  You must understand the job description of the
                                            position that you are applying for and must shape your resume accordingly on the same
                                            lines to maximize your chances of getting shortlisted.</p>


                                <p><b>5. Structured Information:</b></br> It is essential for the resume to be clean, neat and simple so as to
                                    be easier to scan through. Since a freshers resume does not have much professional
                                    experiences to add on, an intelligent approach is required. Rather than filling the resume
                                    randomly, a proper structure should be maintained.</p> <br>
                                    
                                    
                                <h5>What to include in your resume for Freshers?</h5><br>
                                
                                <P><b>1.Your Profile: </b></br> The first section of your resume must state your name, all your contact details
                                    with your home address, phone number and email address.</P>
                                
                                
                              
                                <div class="row pb-3">
                                    <div class="col-lg-12">
                                        <p><b>2. Include your Social Media Accounts:</b> An additional link under the contact details of your
                                            professional social media accounts would help the recruiters get some additional
                                            information about you, if they wish to seek it out. You could include your LinkedIn account.
                                            Including these types of accounts would portray you as a professional individual.</p>
                                            
                                        </div>
                                    <div class="col-lg-6">
                                        <div class="card-body-img">
                                            <img src="https://i0.wp.com/www.rep-am.com/wp-content/uploads/2020/09/20200903-144808-pic-916558984.jpg?fit=595%2C349&ssl=1" alt="blog-img">
                                        </div>
                                        
                                    </div>
                                </div>

                                
                                 <P><b>3. Write a Powerful Summary:</b></br>  The next section of your resume should be a personal
                                summary that should state who you as an individual and as a professional, what are your
                                career goals and why you are the best candidate for the position you are applying for as well
                                how you can be of value to the company.</P>
            
            
                                
                                <p><b>4. List your Soft Skills and Hard Skills:</b> </br> Mention only those skills that would benefit you and
                                help you do better at the job. For example, if you are applying for a position in content
                                writing mention that you are good at writing, planning and organizing. If you have applied
                                for a customer service role, include your communication, interpersonal and conflict
                                management skills etc.</p>
                                
                                <p><b>5. Mention your Educational Background:</b> </br> List down your formal qualifications as well as any
                                certifications that you have. Mention whether you have been any top achiever or won
                                awards or scholarships.</p>
                                
                                <p><b>6. Give something about an informal work experience:</b> </br> If you have ever volunteered or done
                                some project work then it is good to mention that. It gives a sense of responsibility that you
                                have worked towards.</p>
                                
                                <p><b>7. Mention your Hobbies and Interests:</b></br>  Hobbies and interests help the recruiters understand
                                what kind of person you are. For instance, if you are an introvert then you would like indoor
                                games.</p>
                                
                                <p><b>8. Highlight your additional skills:</b></br>  These can be skills such as any fluency in any foreign
                                language or any other skill that you would seem fit for the role.</p>
                                
                                <P><b>9. Declare your willingness to learn new skills:</b></br>  This would help portray you as a candidate who
                                is willing to learn new skills and help draw the attention of recruiters.</P>

                                
                                <div class="row pb-3">
                                    <div class="col-lg-6">
                                        <p><b>10. Proofread:</b></br>  It is advisable to re-read your resume and check for any errors, omissions,
                                            grammatical or spelling mistakes. You can also check whether there is any room for
                                            improvement or any additional item is required to add.</p>
